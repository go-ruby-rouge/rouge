var sqlLexer = func() *RegexLexer {
	b := newRegexLexer("sql", "SQL")
	b.filenames("*.sql")

	b.state("root").
		rule(`\s+`, Text).
		rule(`--.*`, CommentSingle).
		rule(`/\*`, CommentMultiline, push("multiline_comments")).
		rule(`\d+`, LiteralNumberInteger).
		rule(`'`, LiteralStringSingle, push("single_string")).
		rule(`"`, NameVariable, push("double_string")).
		rule("`", NameVariable, push("backtick")).
		cb(`\w+`, func(l *lexState, m *onigmo.MatchData) {
			word := strings.ToUpper(m.Str(0))
			switch {
			case sqlKeywordsType[word]:
				l.emit(NameBuiltin, m.Str(0))
			case sqlKeywords[word]:
				l.emit(Keyword, m.Str(0))
			default:
				l.emit(Name, m.Str(0))
			}
			l.pos = m.End(0)
		}).
		rule(`[+*/<>=~!@#%&|?^-]`, Operator).
		rule(`[;:()\[\]\{\},.]`, Punctuation)

	b.state("multiline_comments").
		rule(`/[*]`, CommentMultiline, push("multiline_comments")).
		rule(`[*]/`, CommentMultiline, pop()).
		rule(`[^/*]+`, CommentMultiline).
		rule(`[/*]`, CommentMultiline)

	b.state("backtick").
		rule(`\\.`, LiteralStringEscape).
		rule("``", LiteralStringEscape).
		rule("`", NameVariable, pop()).
		rule("[^\\\\`]+", NameVariable)

	b.state("single_string").
		rule(`\\.`, LiteralStringEscape).
		rule(`''`, LiteralStringEscape).
		rule(`'`, LiteralStringSingle, pop()).
		rule(`[^\\']+`, LiteralStringSingle)

	b.state("double_string").
		rule(`\\.`, LiteralStringEscape).
		rule(`""`, LiteralStringEscape).
		rule(`"`, NameVariable, pop()).
		rule(`[^\\"]+`, NameVariable)

	return b.done()
}()
